  public List<RouteMethod> parse(Jooby app, final List<Route.Definition> routes) throws Exception {
    ClassLoader loader = app.getClass().getClassLoader();
    String classname = app.getClass().getName();

    Map<String, ClassNode> cache = new HashMap<>();
    Function<String, ClassNode> lookup = name -> {
      ClassNode node = null;
      try {
        String cname = name.replace("/", ".");
        node = cache.get(cname);
        if (node == null) {
          ClassReader reader = new ClassReader(cname);
          node = new ClassNode();
          reader.accept(node, 0);
          cache.put(cname, node);
          if (log.isDebugEnabled()) {
            log.debug("Source: {}; Class: {}", node.sourceFile, node.name);
            reader.accept(
                new TraceClassVisitor(null, new ASMifier(), new PrintWriter(writer(log, name))),
                0);
          }
        }
      } catch (Exception x) {
        sneakyThrow(x);
      }
      return node;
    };
    ClassNode owner = lookup.apply(classname);
    List<Lambda> lambdas = bindMethods(owner, lambdas(owner, lookup), lookup);

    /** javadoc parser: */
    Map<String, List<DocItem>> javadocCache = new HashMap<>();
    Function<Route.Definition, Optional<DocItem>> javadoc = route -> {
      Optional<String> declaringClass = route.source().declaringClass();
      if (declaringClass.isPresent()) {
        log.debug("parsing javadoc for {}", declaringClass.get());
        List<DocItem> doc = javadocCache.get(declaringClass.get());
        if (doc == null) {
          try {
            doc = DocParser.javadoc(dir, declaringClass.get());
          } catch (IOException x) {
            log.debug("No javadoc for {}", declaringClass.get(), x);
            doc = Collections.emptyList();
          }
          javadocCache.put(declaringClass.get(), doc);
        }
        List<DocItem> doclist = doc;
        int pos = IntStream.range(0, doclist.size())
            .filter(i -> doclist.get(i).matches(route.method(), route.pattern()))
            .findFirst()
            .orElse(-1);
        if (pos >= 0) {
          return Optional.ofNullable(doclist.remove(pos));
        }
      }
      return Optional.empty();
    };

    List<RouteMethodImpl> methods = new ArrayList<>();
    for (Lambda lambda : lambdas) {
      if (lambda.method.isPresent()) {
        MethodNode method = lambda.method.get();

        java.lang.reflect.Type returnType;
        if (method.desc.endsWith("V")) {
          returnType = void.class;
        } else if (method.desc.endsWith(RETURN_OBJ)) {
          returnType = returnType(loader, method);
        } else {
          returnType = descToJavaType(loader,
              Optional.ofNullable(method.signature).orElse(method.desc));
        }
        List<RouteParameterImpl> parameters = params(loader, owner, method);
        methods.add(new RouteMethodImpl(lambda.name, lambda.pattern,
            new RouteResponseImpl(simplifyType(returnType))).parameters(parameters));
      } else {
        log.warn("can't bind implementation for {} at {}", lambda, lambda.owner);
      }
    }
    BiFunction<String, String, OptionalInt> routeIndex = (verb, pattern) ->
        IntStream.range(0, methods.size())
            .filter(i -> methods.get(i).matches(verb, pattern))
            .findFirst();

    List<RouteMethod> result = new ArrayList<>(routes.size());
    for (Route.Definition route : routes) {
      Optional<DocItem> doc = javadoc.apply(route);
      Filter filter = route.filter();
      if (filter instanceof Route.MethodHandler) {
        result.add(pathvar(route, javadoc(new RouteMethodImpl(route), doc)));
      } else {
        routeIndex.apply(route.method(), route.pattern())
            .ifPresent(i -> result.add(pathvar(route, javadoc(methods.remove(i), doc))));
      }
    }
    return result;
  }

  private RouteMethodImpl pathvar(Route.Definition route, final RouteMethodImpl method) {
    method.parameters().forEach(p -> {
      if (route.vars().contains(p.name())) {
        p.kind(RouteParameter.Kind.PATH);
      }
    });
    return method;
  }

  private RouteMethodImpl javadoc(final RouteMethodImpl method, final Optional<DocItem> doc) {
    doc.ifPresent(it -> {
      method.description(it.text);
      method.summary(it.summary);
      method.returns(it.returns);
      method.status(it.statusCodes);
      method.parameters().forEach(p -> {
        p.description(it.parameters.get(p.name()));
      });
    });
    return method;
  }

  @SuppressWarnings("unchecked")
  private List<Lambda> bindMethods(final ClassNode owner, final List<Lambda> lambdas,
      final Function<String, ClassNode> lookup) {
    // Find target bindMethods for each lambda expression
    List<MethodNode> methods = owner.methods;
    return lambdas.stream()
        .flatMap(it -> {
          if (it.method.isPresent()) {
            return Stream.of(it);
          }
          return methods.stream()
              .filter(m -> owner.name.equals(it.owner) && it.implementationName.equals(m.name)
                  && it.desc.equals(m.desc))
              .findFirst()
              .map(m -> Stream.of(it.method(m)))
              .orElseGet(() -> {
                ClassNode jump = lookup.apply(it.owner);
                List<Lambda> ext = bindMethods(jump, Arrays.asList(it), lookup);
                return ext.stream();
              });
        })
        .collect(Collectors.toList());
  }

  @SuppressWarnings("unchecked")
  private List<Lambda> lambdas(final ClassNode owner, final Function<String, ClassNode> lookup) {
    if (owner.sourceFile.endsWith(".kt")) {
      return kotlinLambdas(owner, lookup);
    } else {
      List<MethodNode> methods = owner.methods;
      List<Lambda> handles = methods.stream().flatMap(method -> {
        List<Lambda> result = new ArrayList<>();
        new Insns(method)
            // get(pattern, ); or post, put, etc...
            .on(InvokeDynamicInsnNode.class, it -> {
              it.next()
                  .filter(is(MethodInsnNode.class))
                  .map(MethodInsnNode.class::cast)
                  .filter(scriptRoute())
                  .findFirst()
                  .ifPresent(m -> Lambda.create(it.node, null)
                      .forEach(result::add));
            })
            // use(new App());
            .on(mount(owner.name), it -> {
              it.prev()
                  .filter(and(is(MethodInsnNode.class), opcode(INVOKESPECIAL)))
                  .findFirst()
                  .map(MethodInsnNode.class::cast)
                  .ifPresent(node -> {
                    List<Lambda> lambdas = lambdas(lookup.apply(node.owner), lookup);
                    Insn.ldcFor(node).stream()
                        .map(e -> e.cst.toString())
                        .findFirst()
                        .ifPresent(prefix -> {
                          IntStream.range(0, lambdas.size())
                              .forEach(i -> lambdas.set(i, lambdas.get(i).prefix(prefix)));
                        });
                    result.addAll(lambdas);
                  });
            })
            .forEach();
        return result.stream();
      }).collect(Collectors.toList());
      return handles;
    }
  }

  @SuppressWarnings("unchecked")
  private List<Lambda> kotlinLambdas(final ClassNode owner,
      final Function<String, ClassNode> lookup) {
    List<Lambda> result = new ArrayList<>();
    List<InnerClassNode> innerClasses = owner.innerClasses;
    for (InnerClassNode innerClass : innerClasses) {
      ClassNode innerNode = lookup.apply(innerClass.name);
      List<MethodNode> methods = innerNode.methods;
      methods.stream()
          .filter(method("invoke", "org.jooby.Jooby")
              .or(method("invoke", "org.jooby.Kooby")))
          .findFirst()
          .ifPresent(method -> {
            new Insns(method)
                .on(scriptRoute(), it -> {
                  it.prev()
                      .filter(and(is(FieldInsnNode.class), opcode(GETSTATIC)))
                      .findFirst()
                      .map(FieldInsnNode.class::cast)
                      .ifPresent(field -> {
                        ClassNode lambda = lookup.apply(field.owner);
                        lambda.methods.stream()
                            .filter(and(
                                methodName("<init>").negate(),
                                methodName("<clinit>").negate()))
                            /**
                             * Kotlin lambdas have two implementation bindMethods:
                             * 1) generic 2) specific
                             * As far we can see, the 2nd method (specific) is the one we need:
                             */
                            .skip(1)
                            .forEach(e -> {
                              Lambda.create(field.owner, Optional.empty(), it.node, (MethodNode) e)
                                  .forEach(result::add);
                            });
                      });
                })
                .on(call("org.jooby.Kooby", "route", String.class,
                    "kotlin.jvm.functions.Function1"), it -> {
                  Optional<String> prefix = Insn.ldcFor(it.node).stream()
                      .map(e -> e.cst.toString())
                      .findFirst();
                  it.prev()
                      .filter(and(is(FieldInsnNode.class), opcode(GETSTATIC)))
                      .findFirst()
                      .map(FieldInsnNode.class::cast)
                      .ifPresent(field -> {
                        ClassNode lambda = lookup.apply(field.owner);
                        lambda.methods.stream()
                            .filter(method("invoke", "org.jooby.KRouteGroup"))
                            .forEach(m -> {
                              new Insns((MethodNode) m)
                                  .on(scriptRoute(), n -> {
                                    n.prev()
                                        .filter(and(is(FieldInsnNode.class), opcode(GETSTATIC)))
                                        .findFirst()
                                        .map(FieldInsnNode.class::cast)
                                        .ifPresent(f -> {
                                          ClassNode route = lookup.apply(f.owner);
                                          route.methods.stream()
                                              .filter(and(
                                                  methodName("<init>").negate(),
                                                  methodName("<clinit>").negate(),
                                                  method("invoke", Object.class).negate()))
                                              .forEach(e -> Lambda
                                                  .create(f.owner, prefix, n.node, (MethodNode) e)
                                                  .forEach(result::add)
                                              );
                                        });
                                  }).forEach();
                            });
                      });
                })
                // use(Jooby())
                .on(mount(Jooby.class.getName()), it -> {
                  it.prev()
                      .filter(and(is(MethodInsnNode.class), opcode(INVOKESPECIAL)))
                      .findFirst()
                      .map(MethodInsnNode.class::cast)
                      .ifPresent(n -> result.addAll(lambdas(lookup.apply(n.owner), lookup)));
                })
                .forEach();
          });
      ;
    }
    return result;
  }

  private List<RouteParameterImpl> params(final ClassLoader loader, final ClassNode owner,
      final MethodNode lambda) {
    List<RouteParameterImpl> result = new ArrayList<>();

    new Insns(lambda)
        .on(param(), it -> {
          String name = parameterName(loader, it.method, it.node)
              .orElse("arg" + result.size());

          AbstractInsnNode next = it.node.getNext();
          Object value = paramValue(loader, owner, lambda, next);
          if (value != next) {
            // there is a default value, move next
            next = next.getNext();
          } else {
            value = null;
          }

          java.lang.reflect.Type parameterType = parameterType(loader, next);
          // boolean are ICONST_0 or ICONST_1
          if (boolean.class.equals(parameterType) && Integer.class.isInstance(value)) {
            value = (((Integer) value)).intValue() == 1;
          }
          result.add(new RouteParameterImpl(name, kind(it.node.name),
              simplifyType(parameterType), value));
        }).forEach();

    return result;
  }

  private RouteParameter.Kind kind(final String name) {
    if (name.equals("header")) {
      return RouteParameter.Kind.HEADER;
    }
    if (name.equals("param") || name.equals("params")) {
      return RouteParameter.Kind.QUERY;
    }
    if (name.equals("form")) {
      return RouteParameter.Kind.FORM;
    }
    if (name.equals("file") || name.equals("files")) {
      return RouteParameter.Kind.FILE;
    }
    return RouteParameter.Kind.BODY;
  }

  private Object paramValue(final ClassLoader loader, final ClassNode owner,
      final MethodNode method, final AbstractInsnNode n) {
    if (n instanceof LdcInsnNode) {

      Object cst = ((LdcInsnNode) n).cst;
      if (cst instanceof Type) {
        boolean typePresent = new Insn<>(method, n)
            .next()
            .filter(is(MethodInsnNode.class))
            .findFirst()
            .map(MethodInsnNode.class::cast)
            .filter(mutantToSomething().or(getOrCreateKotlinClass()))
            .isPresent();
        if (typePresent) {
          return null;
        }
        return loadType(loader, ((Type) cst).getClassName());
      }
      return cst;
    } else if (n instanceof InsnNode) {
      InsnNode insn = (InsnNode) n;
      switch (insn.getOpcode()) {
        case Opcodes.ICONST_0:
          return 0;
        case Opcodes.ICONST_1:
          return 1;
        case Opcodes.ICONST_2:
          return 2;
        case Opcodes.ICONST_3:
          return 3;
        case Opcodes.ICONST_4:
          return 4;
        case Opcodes.ICONST_5:
          return 5;
        case Opcodes.LCONST_0:
          return 0L;
        case Opcodes.LCONST_1:
          return 1L;
        case Opcodes.FCONST_0:
          return 0f;
        case Opcodes.FCONST_1:
          return 1f;
        case Opcodes.FCONST_2:
          return 2f;
        case Opcodes.DCONST_0:
          return 0d;
        case Opcodes.DCONST_1:
          return 1d;
        case Opcodes.ICONST_M1:
          return -1;
        case Opcodes.ACONST_NULL:
          return null;
      }
    } else if (n instanceof IntInsnNode) {
      IntInsnNode intinsn = (IntInsnNode) n;
      return intinsn.operand;
    } else if (n instanceof FieldInsnNode) {
      // toEnum
      FieldInsnNode finsn = (FieldInsnNode) n;
      if (finsn.getOpcode() == GETSTATIC) {
        java.lang.reflect.Type possibleEnum = loadType(loader, finsn.owner);
        if (MoreTypes.getRawType(possibleEnum).isEnum()) {
          return finsn.name;
        }
      }
    }
    return n;
  }

  private java.lang.reflect.Type parameterType(final ClassLoader loader, final AbstractInsnNode n) {
    if (n instanceof MethodInsnNode) {
      MethodInsnNode node = (MethodInsnNode) n;
      if (mutantValue().test(node)) {
        /** value(); intValue(); booleanValue(); */
        return descToJavaType(loader, node.desc);
      } else if (mutantToSomething().test(node)
          || getOrCreateKotlinClass().test(node)) {
        /** to(String.class); toOptional; toList(); */
        String owner = Type.getReturnType(node.desc).getClassName();
        AbstractInsnNode prev = node.getPrevious();
        if (prev instanceof FieldInsnNode) {
          /** toEnum(Letter.A); */
          return loadType(loader, ((FieldInsnNode) prev).owner);
        }
        java.lang.reflect.Type toType = String.class;
        if (prev instanceof LdcInsnNode) {
          /** to(Foo.class); */
          Object cst = ((LdcInsnNode) prev).cst;
          if (cst instanceof Type) {
            toType = loadType(loader, ((Type) cst).getClassName());
          }
        }

        Set<String> skipOwners = ImmutableSet.of(
            Object.class.getName(),
            Enum.class.getName(),
            "kotlin.reflect.KClass");
        if (skipOwners.contains(owner)) {
          return toType;
        }

        /** toList(Foo.class); */
        return Types.newParameterizedType(loadType(loader, owner), toType);
      }
    } else if (n instanceof VarInsnNode) {
      return new Insn<>(null, n)
          .prev()
          .filter(is(MethodInsnNode.class))
          .findFirst()
          .map(MethodInsnNode.class::cast)
          .filter(file())
          .map(m -> {
            return m.name.equals("files")
                ? Types.newParameterizedType(List.class, Upload.class)
                : Upload.class;
          }).orElse(Object.class);
    } else if (n instanceof TypeInsnNode) {
      TypeInsnNode typeInsn = (TypeInsnNode) n;
      if (typeInsn.getOpcode() == Opcodes.CHECKCAST) {
        return loadType(loader, typeInsn.desc);
      }
    }
    return Object.class;
  }

  private Optional<String> parameterName(final ClassLoader loader, final MethodNode method,
      final MethodInsnNode node) {
    if (node.name.equals("body") || node.name.equals("form")) {
      return Optional.of(node.name);
    }

    /** Collect all ldc between a previous method invocation and current invocation: */
    List<LdcInsnNode> ldc = Insn.ldcFor(node);
    return ldc.size() > 0 ? Optional.of((String) ldc.get(0).cst) : Optional.empty();
  }

  @SuppressWarnings("unchecked")
  private java.lang.reflect.Type returnType(final ClassLoader loader,
      final MethodNode m) throws Exception {
    return new Insns(m)
        .last()
        .prev()
        .filter(and(is(InsnNode.class), opcode(Opcodes.ARETURN)))
        .findFirst()
        .map(AbstractInsnNode::getPrevious)
        .map(previous -> {
          /** return 1; return true; return new Foo(); */
          if (previous instanceof MethodInsnNode) {
            MethodInsnNode methodNode = ((MethodInsnNode) previous);
            if (methodNode.name.equals("<init>")) {
              return loadType(loader, methodNode.owner);
            }
            String desc = methodNode.desc;
            return descToJavaType(loader, desc);
          }
          /** return "String" | int | double */
          if (previous instanceof LdcInsnNode) {
            Object cst = ((LdcInsnNode) previous).cst;
            if (cst instanceof Type) {
              return descToJavaType(loader, ((Type) cst).getDescriptor());
            }
            return cst.getClass();
          }
          /** return variable */
          if (previous instanceof VarInsnNode) {
            VarInsnNode varInsn = (VarInsnNode) previous;
            return localVariable(loader, m, varInsn);
          }

          return Object.class;
        }).orElse(Object.class);
  }

  @SuppressWarnings("unchecked")
  static java.lang.reflect.Type localVariable(final ClassLoader loader, final MethodNode m,
      final VarInsnNode varInsn) {
    if (varInsn.getOpcode() == Opcodes.ALOAD) {
      List<LocalVariableNode> vars = m.localVariables;
      LocalVariableNode var = vars.stream()
          .filter(v -> v.index == varInsn.var)
          .findFirst()
          .orElse(null);
      if (var != null) {
        String signature = "()" + Optional.ofNullable(var.signature).orElse(var.desc);
        return descToJavaType(loader, signature);
      }
    }
    return Object.class;
  }

  static java.lang.reflect.Type descToJavaType(final ClassLoader loader,
      final String desc) {
    TypeBuilder builder = new TypeBuilder(loader);
    new SignatureReader(desc).accept(builder);
    return builder.getType();
  }

  static java.lang.reflect.Type loadType(final ClassLoader loader, final String name) {
    try {
      return loader.loadClass(name.replace("/", "."));
    } catch (Exception x) {
      sneakyThrow(x);
      return null;
    }
  }

  @SuppressWarnings("unchecked")
  static <E extends Throwable> void sneakyThrow(final Throwable x) throws E {
    throw (E) x;
  }

  static java.lang.reflect.Type simplifyType(final java.lang.reflect.Type type) {
    Class<?> rawType = MoreTypes.getRawType(type);
    if (Primitives.isWrapperType(rawType)) {
      return Primitives.unwrap(rawType);
    }
    return type;
  }

  static Writer writer(final Logger log, final String owner) {
    return new Writer() {
      StringBuilder buff = new StringBuilder();

      @Override
      public void write(final char[] cbuf, final int off, final int len) throws IOException {
        buff.append(cbuf, off, len);
      }

      @Override
      public void flush() throws IOException {
        log.debug("{}:\n{}", owner, buff);
      }

      @Override
      public void close() throws IOException {
      }
    };
  }
